UserModel.watch().on("change", async () => {
  let stats = await StatsModel.find({}).sort({ createdAt: "desc" }).limit(1);

  // Check if stats is empty
  if (stats.length === 0) {
    // If no stats exist, create a new one
    stats = [new StatsModel()];
  }

  const subscription = await UserModel.find({
    "subscription.status": "active",
  });

  stats[0].users = await UserModel.countDocuments();
  stats[0].subscription = subscription.length;
  stats[0].createdAt = new Date(Date.now());

  await stats[0].save();
});
